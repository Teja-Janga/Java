public class SwitchCase {
    public static void main(String[] args)
    {
        int num = 34;
        switch (num)
        {
            case 12:
            System.out.println("a");        
                break;
            case 23:
            System.out.println("w");
                break;
            case 34:
            System.out.println("Hell Yeah! You Got it...");
                break;
            default:
            System.out.println("Better Luck Next Time!");
                break;
        }        
    }
}
